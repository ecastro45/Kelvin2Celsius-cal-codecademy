// Constant variable for Kelvin
const kelvin = prompt(`What is the Kelvins temperature today?`);

// Changable variable for celsius
let celsius = kelvin-273;

// Calculates celsius to Fahrenheit
let Fahrenheit = celsius * (9/5) + 32;

// Console log that outputs string with temperature with rounded temp
console.log(`The temperature is ${Fahrenheit = Math.floor(Fahrenheit)} degrees fahrenheit.`);
